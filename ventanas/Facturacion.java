    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
               try {
        // Captura de datos
        String fecha = jFormattedTextField1.getText().trim();
        String placa = jTextField3.getText().trim();
        String precioString = jTextField4.getText().trim();
        String clienteId = jTextField1.getText().trim();
        String metodoDePago = (String) jComboBox1.getSelectedItem(); 
        String cuotasString = (String) jComboBox2.getSelectedItem();
        
        String financiacion = "";
        if (jCheckBox1.isSelected()) {
            financiacion += "Si, ";
        }
        if (jCheckBox2.isSelected()) {
            financiacion += "No, "; 
        }
        
        if (!financiacion.isEmpty()) {
            financiacion = financiacion.substring(0, financiacion.length() - 2); 
        }
        if (cuotasString.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos requeridos.");
            return;
        }

        if (fecha.isEmpty() || placa.isEmpty() || precioString.isEmpty() || clienteId.isEmpty() ||
            metodoDePago.isEmpty() || cuotasString.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos requeridos.");
            return;
        }
        double precio;
        int cuotas;
        try {
            precio = Double.parseDouble(precioString);
            cuotas = Integer.parseInt(cuotasString); 
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "El precio y las cuotas deben ser números válidos.");
            return;
        }

        Vehiculo vehiculo = new Vehiculo(placa, precio);
        Cliente cliente = new Cliente(clienteId);

        Factura factura = new Factura(fecha, vehiculo, vehiculo, cliente, metodoDePago, financiacion, cuotas);

        FacturaPDF.generarPDF(factura);
        
        javax.swing.JOptionPane.showMessageDialog(this, "Factura generada con éxito!");
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Error al generar la factura: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton5ActionPerformed

